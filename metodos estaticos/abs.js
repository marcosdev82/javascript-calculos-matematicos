// Métodos Estáticos do Objeto Math no JavaScript

// A função Math.abs(x) retorna o valor absoluto de um número "x", tal qual:

Math.abs("-1"); // 1
Math.abs(-2); // 2
Math.abs(null); // 0
Math.abs(""); // 0
Math.abs([]); // 0
Math.abs([2]); // 2
Math.abs([1, 2]); // NaN
Math.abs({}); // NaN
Math.abs("string"); // NaN
Math.abs(); // NaN
